<script>
    // Get the elements
    var minusButton = document.getElementById('minus');
    var plusButton = document.getElementById('plus');
    var quantityInput = document.getElementById('input');
    var priceText = document.getElementById('card-text');
    // var totalPriceElement = document.getElementById('total-price');

    // Get the initial price
    var initialPrice = parseFloat(priceText.textContent.replace('Price: $', ''));

    // Add event listener to the "-" button
    minusButton.addEventListener('click', function() {
        if(quantityInput.value > 0) {
            quantityInput.value--;
            priceText.textContent = 'Price : $' + (initialPrice * quantityInput.value).toFixed(2);
        }
    });

    // Add event listener to the "+" button
    plusButton.addEventListener('click', function() {
        quantityInput.value++;
        priceText.textContent = 'Price : $' + (initialPrice * quantityInput.value).toFixed(2);
    });
</script>
